// Supported translation languages for GramAI

const GRAMAI_LANGUAGES = [
  { code: 'en', name: 'English' },
  { code: 'es', name: 'Spanish' },
  { code: 'fr', name: 'French' },
  { code: 'de', name: 'German' },
  { code: 'it', name: 'Italian' },
  { code: 'pt', name: 'Portuguese' },
  { code: 'nl', name: 'Dutch' },
  { code: 'ru', name: 'Russian' },
  { code: 'zh', name: 'Chinese' },
  { code: 'ja', name: 'Japanese' },
  { code: 'ko', name: 'Korean' },
  { code: 'ar', name: 'Arabic' },
  { code: 'hi', name: 'Hindi' },
  { code: 'tr', name: 'Turkish' },
  { code: 'pl', name: 'Polish' },
];

function getLanguageName(code) {
  const lang = GRAMAI_LANGUAGES.find(l => l.code === code);
  return lang ? lang.name : 'English';
}

if (typeof window !== 'undefined') {
  window.GramAILanguages = { GRAMAI_LANGUAGES, getLanguageName };
}
